namespace Emipro\Smsnotification\Model\ResourceModel;

class Smsstore extends \Magento\Framework\Model\ResourceModel\Db\AbstractDb
{
    /**
     * Date model
     * 
     * @var \Magento\Framework\Stdlib\DateTime\DateTime
     */
    protected $_date;

    /**
     * constructor
     * 
     * @param \Magento\Framework\Stdlib\DateTime\DateTime $date
     * @param \Magento\Framework\Model\ResourceModel\Db\Context $context
     */
    public function __construct(
        \Magento\Framework\Stdlib\DateTime\DateTime $date,
        \Magento\Framework\Model\ResourceModel\Db\Context $context
    )
    {
        $this->_date = $date;
        parent::__construct($context);
    }


    /**
     * Initialize resource model
     *
     * @return void
     */
    protected function _construct()
    {
        $this->_init('emipro_smsnotification_smsevent_store', 'smsevent_id');
    }

    /**
     * Retrieves SMS Event SMS  Title from DB by passed id.
     *
     * @param string $id
     * @return string|bool
     */
   
    public function getcustomRow($id)
    {
        $adapter = $this->getConnection();
        $select = $adapter->select()
            ->from("emipro_smsnotification_smsevent_store")
            ->where('main_entity_id = ?',$id)
            ->where('store_id = ?','2');
        return $adapter->fetchRow($select);
    }
    /**
     * before save callback
     *
     * @param \Magento\Framework\Model\AbstractModel|\Emipro\Smsnotification\Model\Smsevent $object
     * @return $this
     */
    protected function _beforeSave(\Magento\Framework\Model\AbstractModel $object)
    {
        $object->setUpdatedAt($this->_date->date());
        if ($object->isObjectNew()) {
            $object->setCreatedAt($this->_date->date());
        }
        return parent::_beforeSave($object);
    }
}
